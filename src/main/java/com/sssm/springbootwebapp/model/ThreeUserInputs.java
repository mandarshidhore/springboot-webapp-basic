package com.sssm.springbootwebapp.model;

public class ThreeUserInputs {

	private int fi;
	private String si;
	private String ti;

	public int getFi() {
		return fi;
	}

	public void setFi(int fi) {
		this.fi = fi;
	}

	public String getSi() {
		return si;
	}

	public void setSi(String si) {
		this.si = si;
	}

	public String getTi() {
		return ti;
	}

	public void setTi(String ti) {
		this.ti = ti;
	}

}
